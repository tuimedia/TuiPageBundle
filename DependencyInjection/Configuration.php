<?php
namespace Tui\PageBundle\DependencyInjection;

use Symfony\Component\Config\Definition\Builder\TreeBuilder;
use Symfony\Component\Config\Definition\ConfigurationInterface;

class Configuration implements ConfigurationInterface
{
    public function getConfigTreeBuilder()
    {
        $treeBuilder = new TreeBuilder('tui_page');

        $treeBuilder->getRootNode()
            ->fixXmlConfig('search_host')
            ->fixXmlConfig('component')
            ->fixXmlConfig('valid_language')
            ->children()
                ->arrayNode('search_hosts')
                    ->info('Search host(s) (if unset, search will be disabled)')
                    ->beforeNormalization()
                        ->castToArray()
                    ->end()
                    ->scalarPrototype()->end()
                ->end()
                ->scalarNode('search_index')
                    ->info('Search index prefix')
                    ->defaultValue('tuipage')
                ->end()
                ->scalarNode('search_api_key')
                    ->info('Typesense API key')
                ->end()
                ->scalarNode('bulk_index_threshold')
                    ->info('Maximum number of documents to index at once')
                    ->defaultValue(50)
                ->end()
                ->scalarNode('page_class')
                    ->info('The class name of your Page entity')
                    ->defaultValue('App\Entity\Page')
                ->end()
                ->scalarNode('page_data_class')
                    ->info('The class name of your PageData entity')
                    ->defaultValue('App\Entity\PageData')
                ->end()
                ->arrayNode('access_roles')
                    ->info('Access roles')
                    ->children()
                        ->arrayNode('list')
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('delete')
                            ->defaultValue(['ROLE_ADMIN'])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('edit')
                            ->defaultValue(['ROLE_ADMIN'])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('create')
                            ->defaultValue(['ROLE_ADMIN'])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('import')
                            ->defaultValue(['ROLE_ADMIN'])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('export')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('history')
                            ->defaultValue(['ROLE_ADMIN'])
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('retrieve')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('search')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->prototype('scalar')->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('serializer_groups')
                    ->info('Serializer configuration')
                    ->children()
                        ->arrayNode('get_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('list_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('create_request')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('create_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('update_request')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('update_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('search_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('history_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                        ->arrayNode('import_response')
                            ->defaultValue([])
                            ->beforeNormalization()
                                ->castToArray()
                            ->end()
                            ->scalarPrototype()->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('components')
                ->info('Frontend components')
                ->isRequired()
                ->useAttributeAsKey('name')
                ->arrayPrototype()
                        ->children()
                            ->scalarNode('schema')
                                ->info('Path to JSON schema for this component')
                                ->isRequired()
                                ->cannotBeEmpty()
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('valid_languages')
                    ->info('Acceptable language codes (if unset, all languages allowed)')
                    ->beforeNormalization()
                        ->castToArray()
                    ->end()
                    ->scalarPrototype()->end()
                ->end()
            ->end()
        ;

        return $treeBuilder;
    }
}
